"""A script to test the factorial module"""
from factorial import factorial

# Display the factorial of 0
print(factorial(0))

# Display the factorial of 1
print(factorial(1))

# Display the factorial of 2
print(factorial(2))

# Display the factorial of 5
print(factorial(5))

# Display the factorial of -2
print(factorial(-2))

# Display the factorial of a string
print(factorial('foo'))
